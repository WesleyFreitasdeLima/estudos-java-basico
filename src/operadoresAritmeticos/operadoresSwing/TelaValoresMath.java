    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        float n1 = Float.parseFloat(txtNum1.getText());
        float n2 = Float.parseFloat(txtNum2.getText());
        
        float resultado = (float) n1 / n2;
        float raiz = (float) Math.sqrt(n1);
        
        lblDiv.setText(Float.toString(resultado));
        lblRaiz.setText(Float.toString(raiz));
    }//GEN-LAST:event_btnCalcularActionPerformed

